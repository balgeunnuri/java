
public class Calc {
	// 필드 (속성)
	// 표현할 속성이 없음.
	// 필드를 정의할 필요 없음.
	
	// 객체를 만들 필요도 없음
	
	
	// 클래스 메서드 ( static 메서드)
	public static void add(int i, int j) {
		System.out.println(i + j);
		
	}
	
	
	// 인스턴스 메서드(일반메서드)
	public void add(int i, int j, int x) {
		System.out.println(i + j + x);
	}
	
	
	
	
}
